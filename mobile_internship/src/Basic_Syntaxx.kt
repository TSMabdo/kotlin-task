class Basic_Syntaxx {
    fun Basic_Syntax(){
        var myint: Int = 10
        var mydouble: Double = 15.5
        var mystring: String = "hello abdo"
        var myboolean: Boolean = true

        println("initial values")
        println("int:$myint")
        println("double:$mydouble")
        println("string:$mystring")
        println("boolean:$myboolean")

        myint = 25
        mydouble = 20.5
        mystring = "hello world"
        myboolean = false
        println("~~~~~~~~~~~~~~~~~")

        println("updated values")
        println("int:$myint")
        println("double:$mydouble")
        println("string:$mystring")
        println("boolean:$myboolean")

        println("~~~~~~~~~~~~~~~~~~~~~~~~~")

    }
}